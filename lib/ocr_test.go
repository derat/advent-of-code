package lib

import (
	"strings"
	"testing"
)

func TestOCR(t *testing.T) {
	for _, tc := range []struct {
		desc, input, want string
	}{
		{
			"2016/08",
			`
#  # ###   ##    ## #### #    ###   ##  #### #### 
#  # #  # #  #    # #    #    #  # #  # #       # 
#  # #  # #  #    # ###  #    ###  #    ###    #  
#  # ###  #  #    # #    #    #  # #    #     #   
#  # #    #  # #  # #    #    #  # #  # #    #    
 ##  #     ##   ##  #    #### ###   ##  #### #### `,
			"UPOJFLBCEZ",
		},
		{
			"2018/10",
			`
 ####   #####   ######  #####   #####   #####   ######     ###
#    #  #    #  #       #    #  #    #  #    #  #           # 
#       #    #  #       #    #  #    #  #    #  #           # 
#       #    #  #       #    #  #    #  #    #  #           # 
#       #####   #####   #####   #####   #####   #####       # 
#  ###  #       #       #       #       #       #           # 
#    #  #       #       #       #       #       #           # 
#    #  #       #       #       #       #       #       #   # 
#   ##  #       #       #       #       #       #       #   # 
 ### #  #       ######  #       #       #       ######   ###  `,
			"GPEPPPEJ",
		},
		{
			"banner uppercase",
			`
   #    ######   #####  ######  ####### #######  #####  #     #   ###         #
  # #   #     # #     # #     # #       #       #     # #     #    #          #
 #   #  #     # #       #     # #       #       #       #     #    #          #
#     # ######  #       #     # #####   #####   #  #### #######    #          #
####### #     # #       #     # #       #       #     # #     #    #    #     #
#     # #     # #     # #     # #       #       #     # #     #    #    #     #
#     # ######   #####  ######  ####### #        #####  #     #   ###    ##### `,
			"ABCDEFGHIJ",
		},
		{
			"banner uppercase",
			`
#    #  #       #     # #     # ####### ######   #####  ######   #####  #######
#   #   #       ##   ## ##    # #     # #     # #     # #     # #     #    #   
#  #    #       # # # # # #   # #     # #     # #     # #     # #          #   
###     #       #  #  # #  #  # #     # ######  #     # ######   #####     #   
#  #    #       #     # #   # # #     # #       #   # # #   #         #    #   
#   #   #       #     # #    ## #     # #       #    #  #    #  #     #    #   
#    #  ####### #     # #     # ####### #        #### # #     #  #####     #   `,
			// TODO: Fix 'M', 'N'.
			//"KLMNOPQRST",
			"KLHHOPQRST",
		},
		{
			"banner uppercase",
			`
#     # #     # #     # #     # #     # #######
#     # #     # #  #  #  #   #   #   #       # 
#     # #     # #  #  #   # #     # #       #  
#     # #     # #  #  #    #       #       #   
#     #  #   #  #  #  #   # #      #      #    
#     #   # #   #  #  #  #   #     #     #     
 #####     #     ## ##  #     #    #    #######`,
			"UVWXYZ",
		},
		{
			"banner lowercase",
			`
                                                                               
   ##    #####    ####   #####   ######  ######   ####   #    #     #         #
  #  #   #    #  #    #  #    #  #       #       #    #  #    #     #         #
 #    #  #####   #       #    #  #####   #####   #       ######     #         #
 ######  #    #  #       #    #  #       #       #  ###  #    #     #         #
 #    #  #    #  #    #  #    #  #       #       #    #  #    #     #    #    #
 #    #  #####    ####   #####   ######  #        ####   #    #     #     #### 
                                                                               `,
			"ABCDEFGHIJ",
		},
		{
			"banner lowercase",
			`

 #    #  #       #    #  #    #   ####   #####    ####   #####    ####    #####
 #   #   #       ##  ##  ##   #  #    #  #    #  #    #  #    #  #          #  
 ####    #       # ## #  # #  #  #    #  #    #  #    #  #    #   ####      #  
 #  #    #       #    #  #  # #  #    #  #####   #  # #  #####        #     #  
 #   #   #       #    #  #   ##  #    #  #       #   #   #   #   #    #     #  
 #    #  ######  #    #  #    #   ####   #        ### #  #    #   ####      #  
                                                                               `,
			// TODO: Fix 'M', 'N', O'.
			//"KLMNOPQRST",
			"KLHHGPQRST",
		},
		{
			"banner lowercase",
			`

 #    #  #    #  #    #  #    #   #   #  ######
 #    #  #    #  #    #   #  #     # #       # 
 #    #  #    #  #    #    ##       #       #  
 #    #  #    #  # ## #    ##       #      #   
 #    #   #  #   ##  ##   #  #      #     #    
  ####     ##    #    #  #    #     #    ######
                                               `,
			// TODO: Fix 'W'.
			//"UVWXYZ",
			"UVHXYZ",
		},
	} {
		got := OCR(ByteLines(strings.TrimLeft(tc.input, "\n"), '#', ' '), ' ')
		if got != tc.want {
			t.Errorf("OCR([%s]) = %q; want %q", tc.desc, got, tc.want)
		}
	}
}
